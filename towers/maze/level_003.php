$this->description("Find stair to escape from maze.");
$this->tip("Call \$warrior->walk() to walk forward in the Player 'play_turn' method.");
$this->time_bonus(15);
$this->ace_score(10);
$this->size(33, 33);
$this->stairs(31, 31);
$this->warrior(1, 1, ':east')->add_abilities(['walk','feel','pivot']);


$maze = new \PHPMaze\PHPMaze(3);
$maze = $maze->generate(33);

$y = 0;
foreach ($maze as $line) {
  $x = 0;
  foreach ($line as $cell) {
    if ($cell) {
      $this->unit(':stone', $x, $y, ':west');
    }
    $x++;
  }
  $y++;
}
